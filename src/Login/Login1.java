        private void login() throws SQLException, ClassNotFoundException {
          
        Connection connect = new Connection();
        Statement stmt = connect.con.createStatement();
        
        String SQL = "SELECT * FROM user where name ='" + jTextField2.getText() + "' and password = '" + jPasswordField1.getText() + "'";
       
        ResultSet results = stmt.executeQuery(SQL);
         
        if (results.next()) {
            registerUser(results.getString("id_user"));
            if (results.getString("password").equals("lahyani")) {
               after_auth fn = new after_auth();
                System.out.println("qsfs");
                        fn.setVisible(true);
            }
            this.dispose();
        } else {
            jLabel6.setVisible(true);
            jLabel6.setText("Invalid Username or password");
        }
    }
        private void registerUser(String id) {
        try {
            FileWriter fw = new FileWriter("user.txt");
            PrintWriter pw = new PrintWriter(fw);
            pw.print(id);
            pw.close();
        } catch (IOException ex) {
            System.out.println("Error");
        }
        }
    
